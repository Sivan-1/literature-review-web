"""
提示词构建器 - 强化学术文献综述格式
"""

def build_prompt(research_topic, text_content):
    """
    构建发送给 DeepSeek API 的提示词
    严格按照学术文献综述格式
    """
    # 限制文本长度
    max_text_length = 15000
    if len(text_content) > max_text_length:
        text_content = text_content[:max_text_length] + "...(内容已截断)"
    
    prompt = f"""你是一位资深的学术研究专家，现在需要撰写一份高质量的文献综述。

研究主题：{research_topic}

提供的文献资料：
{text_content}

请严格按照以下学术文献综述的规范格式撰写，确保每个部分都有明确的标题，段落清晰，参考文献格式规范。

【输出格式要求】

请按照以下结构输出，每个部分必须使用明确的标题：

一、研究背景与意义
（在此部分详细阐述研究背景、理论意义和实践价值）

二、国内外研究现状
（在此部分系统梳理现有研究成果，分类阐述主要研究方向）

三、主要研究进展与发现
3.1 研究方法与技术路线
（总结现有研究采用的主要方法）
3.2 核心研究成果
（归纳主要研究发现和结论）

四、研究热点与前沿趋势
4.1 当前研究热点
（分析近期研究关注的重点方向）
4.2 前沿技术动态
（介绍最新的技术发展）

五、存在的问题与挑战
（客观分析现有研究的不足和面临的困难）

六、未来研究方向建议
（提出有建设性的研究展望）

七、结论
（总结全文要点）

八、参考文献
（严格按照以下格式列出所有引用的文献）

【参考文献格式要求】
- 中文期刊：[序号] 作者. 文章标题[J]. 期刊名称, 年份, 卷(期): 起止页码.
- 英文期刊：[序号] Author. Article Title[J]. Journal Name, Year, Volume(Issue): Pages.
- 中文书籍：[序号] 作者. 书名[M]. 出版地: 出版社, 年份.
- 英文书籍：[序号] Author. Book Title[M]. Place: Publisher, Year.
- 学位论文：[序号] 作者. 论文标题[D]. 所在城市: 学校名称, 年份.
- 会议论文：[序号] 作者. 论文标题[C]. 会议名称, 地点, 年份.

【格式示例】
[1] 董雪艳, 陈锋, 赵健宇, 等. AI技术可供性驱动国有企业财务数智化价值创新的过程机理——基于中石化的探索性案例研究[J]. 管理世界, 2026, 42(3): 1-28.
[2] 王建国, 李明. 人工智能在医疗诊断中的应用研究[J]. 中国医学影像技术, 2025, 41(2): 123-135.
[3] Smith J, Johnson M. Deep Learning in Healthcare: A Comprehensive Review[J]. Nature Medicine, 2024, 30(5): 456-470.

【重要说明】
1. 每个部分标题必须单独占一行，使用"一、"、"二、"等中文数字编号
2. 段落之间必须有空行分隔
3. 参考文献每一条单独占一行，使用[1]、[2]等编号
4. 不要使用任何Markdown格式（不要用**、*、#等符号）
5. 确保内容充实、专业，符合学术规范

现在请开始撰写文献综述："""
    
    return prompt